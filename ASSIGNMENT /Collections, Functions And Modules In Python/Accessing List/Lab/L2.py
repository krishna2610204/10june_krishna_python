"""Write a Python program to access elements at different index positions."""

list1 = ["Banana" , "Mango" , "Orange" , "Apple" , "Pineapple"]

print(list1[0]) #Position Wise
print(list1[:2]) # Will print till index no.2
print(list1[:]) # will print the whole index
print(list1[-2]) # Negative index
print(list1[2:4])   # ['Orange', 'Apple']
print(list1[-1])    # Pineapple
print(list1[1:])    # From Mango to end
print(list1[:-1])   # All except last element

