"""Write a Python program to separate keys and values from a dictionary using
keys() and values() methods."""

info = {'Name' : 'Krishna' , 
        'Age' : 20 , 
        'College name' : 'MArwadi University' , 
        'Current CGPA' : 8.23 ,
        'Course' : 'BCA',
        'Hobbies' : 'Dancing , Swimming , Badmintion',
        'Current Profession' : 'Student'}

print("Keys : " ,info.keys())

print("Values : " , info.values())

