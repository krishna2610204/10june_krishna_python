# Integer
age = 25
print(age)

# Float
price = 99.99
print(price)

# String
name = "Krishna"
print(name)

# List
fruits = ["apple", "banana", "mango"]
print(fruits)

# Tuple
colors = ("red", "green", "blue")
print(colors)

# Dictionary
student = {
    "name": "Asha",
    "age": 20,
    "grade": "A"
}
print(student)

# Set
unique_numbers = {1, 2, 3, 2, 1}
print(unique_numbers)

# Boolean
is_active = True
print(is_active)
